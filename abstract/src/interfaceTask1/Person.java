package interfaceTask1;

public class Person {

	public String name = "";
	public int age = 0;
	
	public Person() {;}

	public Person(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	// 먹기, 등교하기, 회사가기
	
	
	
}
